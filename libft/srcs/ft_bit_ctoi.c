#include "libft.h"

int	ft_bit_ctoi(char *oct)
{
	int nb;
	int i;

	nb = 0;
	i = 1;
	nb -= ((*oct - '0') * ft_pow(2, 7));
	while (i < 8)
	{
		nb += ((oct[i] - '0') * ft_pow(2, 7 - i));
		i++;
	}
	return (nb);
}
