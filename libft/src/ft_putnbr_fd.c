#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nb;

	if (fd < 0)
		return ;
	nb = (n < 0 ? -n : n);
	if (n < 0)
		write(fd, "-", 1);
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putchar_fd(nb % 10 + '0', fd);
	}
	else
		ft_putchar_fd(nb % 10 + '0', fd);
}
